export function getFormulacaoById(id: string): Formulacao | undefined {
  return formulacoes.find((f) => f.id === id);
}

export function getLinhas(): { linha: string; linhaSlug: string; acento_cor: string; count: number }[] {
  const map = new Map<string, { linha: string; linhaSlug: string; acento_cor: string; count: number }>();
  for (const f of formulacoes) {
    if (!map.has(f.linhaSlug)) {
      map.set(f.linhaSlug, { linha: f.linha, linhaSlug: f.linhaSlug, acento_cor: f.acento_cor, count: 0 });
    }
    map.get(f.linhaSlug)!.count++;
  }
  return Array.from(map.values());
}
